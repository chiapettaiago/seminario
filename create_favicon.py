# Criar um arquivo PNG mínimo manualmente
# Cabeçalho PNG + dados mínimos para um ícone simples 16x16
def create_simple_favicon():
    # Este é um PNG válido muito básico
    minimal_png = bytes([
        0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,  # PNG signature
        0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,  # IHDR chunk
        0x00, 0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x10,  # 16x16 dimensions
        0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0xF3, 0xFF,  # 8-bit RGBA
        0x61, 0x00, 0x00, 0x00, 0x06, 0x62, 0x4B, 0x47,  # bKGD chunk
        0x44, 0x00, 0x4C, 0x00, 0xAF, 0x00, 0x50, 0x81,  # background green
        0xB5, 0x4A, 0x00, 0x00, 0x00, 0x13, 0x49, 0x44,  # IDAT chunk start
        0x41, 0x54, 0x78, 0x9C, 0x62, 0xF8, 0xCF, 0xC0,  # compressed data
        0xF0, 0x9F, 0x81, 0x81, 0x01, 0x00, 0x00, 0x0A,  # green pixels
        0x00, 0x01, 0xB5, 0x04, 0x88, 0x45, 0x00, 0x00,  # end data
        0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42,  # IEND chunk
        0x60, 0x82
    ])
    
    with open('/home/iago/seminario/static/favicon.png', 'wb') as f:
        f.write(minimal_png)
    print("Favicon PNG criado com sucesso!")

create_simple_favicon()